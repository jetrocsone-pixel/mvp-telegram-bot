from openai import OpenAI

from app.config import OPENAI_API_KEY

client = OpenAI(api_key=OPENAI_API_KEY)


def generate_tz_lite(image_url):
    prompt = """
Ты — эксперт по созданию продающих фотоворонок для маркетплейсов (Wildberries, Ozon).

Задача: по ОДНОЙ фотографии товара создать ТЗ для дизайнера.

---

1) ОПРЕДЕЛИ (внутри, не выводить):

- что это за товар
- его назначение
- целевую аудиторию (только очевидные выводы)

---

2) ОПРЕДЕЛИ ПОВЕДЕНЧЕСКИЙ ТИП ТОВАРА (НЕ ВЫВОДИТЬ):

- одежда
- визуал / эмоция
- рационал / техника
- утилитарка / решение проблемы
- дом / мебель
- товарка / подарок

И строй воронку, исходя из логики этого типа:

- одежда → посадка / образ / детали / размер
- техника → характеристики / применение / совместимость / безопасность
- утилитарка → проблема / решение / процесс / результат
- мебель → визуал / функция / габариты / доверие
- товарка → эстетика / атмосфера / упаковка
- визуал → “хочу” через картинку

---

3) ОПРЕДЕЛИ 3 КЛЮЧЕВЫХ СМЫСЛА

(главное, что продаёт товар)

Именно они должны лечь в основу первых слайдов.

---

4) СФОРМИРУЙ ВОРОНКУ ЧЕРЕЗ УГЛЫ ПОДАЧИ

Используй 4–6 разных углов:

- результат / эффект
- проблема → решение
- сценарий использования
- демонстрация процесса
- сравнение
- конструкция / внутрянка
- параметры / габариты
- универсальность
- премиальность / стиль
- минимализм (чистый визуал)
- детали / качество
- безопасность / надежность
- упаковка / доставка
- гарантия / доверие

ВАЖНО:
- первые 5 слайдов = 5 разных углов
- не повторять одинаковые смыслы
- не использовать шаблонную структуру

---

5) ОПРЕДЕЛИ КОЛИЧЕСТВО СЛАЙДОВ:

- простой товар → 5–6
- средний → 6–8
- сложный / техника / одежда → 8–10

---

6) СФОРМИРУЙ ТЗ

🔹 СЛАЙДЫ 1–5 (ПОДРОБНО)

Слайд 1 — ...
Слайд 2 — ...
Слайд 3 — ...
Слайд 4 — ...
Слайд 5 — ...

Для каждого:

- Что изображено:
- Текст на изображении:
- Цель слайда:

ТРЕБОВАНИЯ К ТЕКСТУ:
- 1–2 короткие строки
- конкретика (не “высокое качество”)
- читается за 1–2 секунды
- без клише

ЕСЛИ ВИЗУАЛ СИЛЬНЫЙ:
- можно без текста

---

🔹 СЛАЙДЫ 6+ (КРАТКО)

Формат строго:

Слайд 6 — ...
Слайд 7 — ...
Слайд 8 — ...

(1 строка = 1 смысл, без пояснений)

Эти слайды должны:
- закрывать возражения
- давать конкретику
- усиливать доверие

---

7) ОБЯЗАТЕЛЬНЫЕ БЛОКИ (ЕСЛИ УМЕСТНЫ)

НЕ писать точные цифры, но указывать:

- размеры / габариты
- характеристики / мощность / объем
- комплектация
- упаковка
- гарантия / возврат
- инструкция / применение

---

8) ПРАВИЛА (ЖЁСТКО)

ЗАПРЕЩЕНО:
- выдумывать характеристики
- использовать один шаблон
- повторять структуру
- делать одинаковые по смыслу слайды подряд

НЕ ДЕЛАТЬ:
- “обложка → преимущества → размеры” без анализа

КАЖДЫЙ РАЗ:
- новая структура
- новая комбинация углов
- новая логика

---

9) ПРОВЕРКА

Перед ответом:
- убери выдуманные факты
- убери слабые формулировки
- проверь, что слайды разные по смыслу

---

10) ДОПОЛНИТЕЛЬНО

Альтернативная концепция (кратко):

Слайд 1 — ...
Слайд 2 — ...
Слайд 3 — ...
Слайд 4 — ...

(другой угол подачи, без повторения основной логики)

---

Ответ:
четкий, структурный, без лишнего текста.
"""

    response = client.responses.create(
        model="gpt-5-mini",
        input=[
            {
                "role": "user",
                "content": [
                    {"type": "input_text", "text": prompt},
                    {"type": "input_image", "image_url": image_url}
                ]
            }
        ]
    )

    return response.output_text